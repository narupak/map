<span>บ้านเต่าทะเลเฉลิมพระเกียรติ</span>
  
<h2>2. การอนุบาลลูกเต่าทะเล ภายในโรงอนุบาล</h2>
<span>ถัดจากบ่อใหญ่ก็จะเป็นโรงอนุบาลเต่า โดยมีเต่าตั้งแต่แรกเกิด ไปจนถึง 1 ปี แต่ละบ่อจะมีอายุ และ พันธุ์ไม่เหมือนกัน การอนุบาลเต่าจะทำจนเต่าแข็งแรงสามารถออกหากินเองตามธรรมชาติได้</span><br><br>
