<h2> 3. จุดพักผ่อนบริเวณชายหาด</h2>
<span>ในพื้นที่ของศูนย์อนุรักษ์พันธุ์เต่าทะเลกองทัพเรือ มีร้านค้าสมาคมภริยาทหารเรือ หาดทราย และ ท่าเรือ หาดสะอาดแต่น้ำทะเลไม่ใสเท่าไหร่ เป็นจุดพักผ่อนสำหรับนักท่องเที่ยวที่มาเยี่ยมชม</span><br>
<img src="http://localhost/CodeIgniter/Img/taotalay6.jpg" id = "image_home" alt=""><br>  
<span>มีเรือลำใหญ่ เรือขนส่งสินค้าจอดเทียบท่าที่ฐานทัพเรือจุกเสม็ด ที่เดียวกับที่เรือหลวงจักรีนฤเบศรจอดอยู่</span><br>
<h2>4. ห้องแสดงนิทรรศการ ภายในอาคารบรรยายสรุป</h2>
<span>เป็นอาคารทรงกลมมีหลังคารูปเต่าทะเล ภายในมีประวัติความเป็นมาของศูนย์อนุรักษ์พันธุ์เต่าทะเลกองทัพเรือ ข้อมูลเต่าทะเลในน่านน้ำไทยว่ามีพันธุ์ไหนบ้าง และสถานการณ์เต่าทะเลในประเทศไทย สามารถเดินชมได้เอง
สำหรับนักท่องเที่ยวที่มาเป็นหมู่คณะสามารถติดต่อเข้าชมวีดีทัศน์เต่าทะเลได้กับเจ้าหน้าที่</span><br>

